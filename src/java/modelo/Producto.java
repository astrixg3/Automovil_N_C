package modelo;

/**
 *
 * @author PabloNote
 */
public class Producto {
    String codigo, nombre, fecha, talla;
    int precio, cantidad;

    public Producto( String nombre, String fecha, String talla, int precio, int cantidad) {
       
        this.nombre = nombre;
        this.fecha = fecha;
        this.talla = talla;
        this.precio = precio;
        this.cantidad = cantidad;
    }

    public String getCodigo() {
        return codigo;
    }

    public void setCodigo(String codigo) {
        this.codigo = codigo;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getFecha() {
        return fecha;
    }

    public void setFecha(String fecha) {
        this.fecha = fecha;
    }

    public String getTalla() {
        return talla;
    }

    public void setTalla(String talla) {
        this.talla = talla;
    }

    public int getPrecio() {
        return precio;
    }

    public void setPrecio(int precio) {
        this.precio = precio;
    }

    public int getCantidad() {
        return cantidad;
    }

    public void setCantidad(int cantidad) {
        this.cantidad = cantidad;
    }

    @Override
    public String toString() {
        return "Producto{" + "codigo=" + codigo + ", nombre=" + nombre + ", fecha=" + fecha + ", talla=" + talla + ", precio=" + precio + ", cantidad=" + cantidad + '}';
    }
    
    
    
}















